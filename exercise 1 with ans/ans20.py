#Question 20: - Write a program which accept days and convert this into years, weeks and days.

DAYS=int(input("Enter the number of DAYS:"))

YEARS=DAYS/365
print("YEARS ARE:",YEARS)

MONTHS=DAYS/30
print("MONTHS ARE:",MONTHS)

WEEKS=DAYS/7
print("WEEKS ARE:",WEEKS)

'''1 week =7 days
1 year =365 days
1 month =30 days'''
